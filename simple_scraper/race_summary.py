class RaceSummary(object):

    def __init__(self, raceId, date, endPosition, horseName, raceType, meetingName, trackDistance, raceGoing, raceClass):
        self.raceId = raceId
        self.date = date
        self.endPosition = endPosition
        self.horseName = horseName
        self.raceType = raceType
        self.meetingName = meetingName
        self.trackDistance = trackDistance
        self.raceGoing = raceGoing
        self.raceClass = raceClass
